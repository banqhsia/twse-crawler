<?php

namespace App\Type;

use PHPUnit\Framework\TestCase;

class ChineseUnitValueTest extends TestCase
{
    /**
     * @testWith [1000, 1000]
     *           [4998, 4998]
     *           [10000, "1 萬"]
     *           [-10000, "-1 萬"]
     *           [10999, "1.1 萬"]
     *           ["10,999", "1.1 萬"]
     *           [10499, "1.05 萬"]
     *           [23000, "2.3 萬"]
     *           [43980, "4.4 萬"]
     *           [-43980, "-4.4 萬"]
     *           [2300000, "230 萬"]
     *           [23000000, "2300 萬"]
     *           [23000000, "2300 萬"]
     *           [23050000, "2305 萬"]
     *           [23058000, "2305.8 萬"]
     *           [23058660, "2305.87 萬"]
     *           [230000000, "2.3 億"]
     *           [2300000000, "23 億"]
     *           [23000000000, "230 億"]
     *           [230000000000, "2300 億"]
     *           [1000000000000, "1 兆"]
     *           [1050000000000, "1.05 兆"]
     *           [1008000000000, "1.01 兆"]
     *           [2300000000000, "2.3 兆"]
     *           [2349999999999, "2.35 兆"]
     *           [23000000000000, "23 兆"]
     *           [-23000000000000, "-23 兆"]
     *           [230000000000000, "230 兆"]
     *           [239999999999999, "240 兆"]
     *           ["230,000,000,000,000", "230 兆"]
     */
    public function test_get($givenValue, $expected)
    {
        $this->target = new ChineseUnitValue($givenValue);

        $this->assertEquals($expected, $this->target->get());
    }
}
